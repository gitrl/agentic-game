/**
 * 根据当前章节号构建应注入 user message 的剧本文本。
 * 包含：截至当前章节的累积真相 + 当前章节锚点。
 */
export function buildChapterContext(chapter: number): string {
  const clampedChapter = Math.min(Math.max(chapter, 1), 5);

  // 真相注入策略：只注入当前层 + 前一层摘要，避免后期 token 膨胀
  const truthParts: string[] = [];
  if (clampedChapter >= 3 && TRUTH_LAYERS[clampedChapter - 1]) {
    // 前一层作为简短背景回顾
    truthParts.push("（前置真相回顾）" + TRUTH_LAYERS[clampedChapter - 1].split("\n")[1]);
  }
  if (TRUTH_LAYERS[clampedChapter]) {
    truthParts.push(TRUTH_LAYERS[clampedChapter]);
  }

  // 当前章节剧本
  const script = CHAPTER_SCRIPTS[clampedChapter - 1];

  const lines: string[] = [
    "# 剧情指导（当前章节）",
    "",
    ...truthParts,
    "",
    `---`,
    "",
    `【第 ${script.chapter} 章 · ${script.title}】${script.turns}`,
    "",
    `真相释放上限：${script.truthCeiling}`,
    "",
    "必经锚点："
  ];

  script.anchors.forEach((a, i) => {
    lines.push(`${i + 1}. ${a}`);
  });

  if (script.redHerrings.length > 0) {
    lines.push("", "可用红鲱鱼（误导性线索，干扰玩家判断但不推进真相）：");
    script.redHerrings.forEach((r) => lines.push(`- ${r}`));
  }

  if (script.doubleEdged.length > 0) {
    lines.push("", "双刃证据（有利但使用不当会反噬）：");
    script.doubleEdged.forEach((d) => lines.push(`- ${d}`));
  }

  lines.push("", `悬念基调：${script.tone}`);

  return lines.join("\n");
}
